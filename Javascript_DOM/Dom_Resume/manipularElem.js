const btn = document.querySelector('button')

// manipulando Html

btn.innerHTML // define ou obtem dentro do elemento
btn.innerText // define ou obtem texto do elemento
btn.textContentContext // define ou obtem conteudo do elemento

// atributos

btn.getAttribute // Obtem o valor do atributo
btn.setAttribute // define o valor do atributo
btn.removeAttribute // remove o atributo
btn.hasAttribute // verifica existencia do atributo

// manipulação de classe

btn.classList.add // adiciona uma classe
btn.classList.remove // remove uma classe
btn.classList.toggle // alterna a classe

// manipulação de estilo :

btn.style // modifica os estilo css de elemento
